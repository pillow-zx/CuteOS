/*
 * kernel/printk.c - 内核日志与格式化输出
 *
 * 功能：
 *   提供内核日志输出基础设施。printk 不区分日志级别，所有输出均直接打印。
 *   vsprintf 作为格式化核心，支持以下转换说明符：
 *     %d  - 有符号十进制整数
 *     %x  - 无符号十六进制整数
 *     %s  - 字符串
 *     %c  - 单个字符
 *     %p  - 指针地址（十六进制）
 *     %%  - 百分号字面量
 *     %ld - long 有符号十进制
 *     %lu - long 无符号十进制
 *     %llx - long long 无符号十六进制
 *     %#x - 带前缀的十六进制（0x）
 *     %-Ns - 左对齐字符串，宽度 N
 *     %0Nd - 零填充十进制，宽度 N
 *
 * 主要函数：
 *   printk(fmt, ...)         - 内核格式化打印，无日志级别过滤，
 *                              所有输出通过底层驱动送达控制台。
 *                              使用 spinlock 保证多核下的输出互斥。
 *   vsprintf(buf, fmt, args) - 格式化核心，将可变参数按格式串写入缓冲区。
 *   panic(fmt, ...)          - 内核致命错误处理：打印 panic 信息，
 *                              输出关键 CSR 寄存器值（sepc/scause/stval/ra/sp）
 *                              用于事后诊断，随后执行 wfi 指令永久挂起 CPU。
 *
 * 调试宏：
 *   ASSERT(cond)   - 调试模式下检查条件，失败则调用 panic；
 *                    发布模式下为空操作（noop）。
 *   BUG_ON(cond)   - 调试模式下若条件为真则触发 panic；
 *                    发布模式下为空操作（noop）。
 */
